export function getFileCategory(filename: string): FileCategory {
  const dotIdx = filename.lastIndexOf('.')
  if (dotIdx < 0) return 'default'
  const ext = filename.slice(dotIdx + 1).toLowerCase()
  return EXT_MAP[ext] || 'default'
}

